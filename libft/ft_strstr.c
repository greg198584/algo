#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int i;
	int j;

	if (s2[0] == '\0' || !s2)
		return ((char *)s1);
	i = 0;
	j = 0;
	while (s1[i] != '\0' && s1)
	{
		j = (j * (s1[i] == s2[j])) + (s1[i] == s2[j]);
		if (s2[j] == '\0')
			return ((char *)s1 + (i - (j - 1)));
		i++;
	}
	return (NULL);
}
